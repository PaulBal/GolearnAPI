const db = require("../models");
const ROLES = db.ROLES;
const Student = db.students;
const Tutor = db.tutors;

exports.checkDuplicateStudent = (req, res, next) => {
  
  Student.findOne({
    username: req.body.username
  }).exec((err, user) => {
    if (err) {
      res.status(500).send({ message: err });
      return;
    }

    if (user) {
      res.status(400).send({ message: "Failed! A student with this username already exists!" });
      return;
    }

    Student.findOne({
      email: req.body.email
    }).exec((err, user) => {
      if (err) {
        res.status(500).send({ message: err });
        return;
      }

      if (user) {
        res.status(400).send({ message: "Failed! Email is already in use!" });
        return;
      }

      next();
    });
  });
};

